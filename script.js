function renderProductos(lista) {
    contenedor.innerHTML = "";

    lista.forEach(p => {
        const card = document.createElement("div");
        card.className = "card";

        card.innerHTML = `
        <div class="img-box">
        <img src="${p.img}" alt="${p.nombre}">
        </div>
        
        <h3>${p.nombre}</h3>
        <p>Rubro: ${p.rubro}</p>
        <p>Precio: $${p.precio}</p>
        `;

        contenedor.appendChild(card);
    });
}
